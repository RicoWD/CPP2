Point::Point() : _x(0), _y(0)
{
}

Point::Point(float nX, float nY) : _x(nX), _y(nY)
{
}

Point::Point(const Point& cpy) : _x(cpy._x), _y(cpy._y)
{
}

Point& Point::operator=(const Point& old)
{
	static_cast<void>(old);
	return (*this);
}

Point::~Point()
{
}

/* ************************************************************************** */
// 																			  //
// 						   				GET									  //
//																	 		  //
/* ************************************************************************** */

Fixed Point::getX()
{
	return (this->_x);
}

Fixed Point::getY()
{
	return (this->_y);
}

